package org.nsider;

public class TriangleSides {
    private double a;
    private double b;
    private double c;

    /* Getter and Setter for side a */
    public double getA() {
        return a;
    }
    public void setA(double a) {
        this.a = a;
    }

    /* Getter and Setter for side b */
    public double getB() {
        return b;
    }
    public void setB(double b) {
        this.b = b;
    }

    /* Getter and setter for side c  */
    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }
}

